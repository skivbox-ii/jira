#!/usr/bin/env node
/**
 * Скрипт сборки ujg-user-activity.js из модулей
 * Использование: node build-user-activity.js
 */

var fs = require("fs");
var path = require("path");

var MODULES_DIR = path.join(__dirname, "ujg-user-activity-modules");
var OUTPUT_FILE = path.join(__dirname, "ujg-user-activity.js");
var TEMP_OUTPUT_FILE = OUTPUT_FILE + ".tmp";

var MODULE_ORDER = [
    "config.js",
    "utils.js",
    "api.js",
    "repo-api.js",
    "data-processor.js",
    "repo-data-processor.js",
    "progress-loader.js",
    "user-picker.js",
    "multi-user-picker.js",
    "date-range-picker.js",
    "summary-cards.js",
    "calendar-heatmap.js",
    "repo-calendar.js",
    "daily-detail.js",
    "unified-calendar.js",
    "project-breakdown.js",
    "issue-list.js",
    "activity-log.js",
    "repo-log.js",
    "rendering.js",
    "main.js"
];

function readModule(fileName) {
    var filePath = path.join(MODULES_DIR, fileName);
    if (!fs.existsSync(filePath)) {
        throw new Error("Module not found: " + fileName);
    }
    return fs.readFileSync(filePath, "utf8");
}

function build() {
    console.log("Building ujg-user-activity.js from modules...");

    var parts = [
        "// Auto-generated file - DO NOT EDIT MANUALLY",
        "// Generated by build-user-activity.js",
        "// To modify, edit files in ujg-user-activity-modules/ and rebuild",
        ""
    ];

    MODULE_ORDER.forEach(function(fileName) {
        var content = readModule(fileName);
        if (!content) return;
        parts.push("/* === Module: " + fileName + " === */");
        parts.push(content.trim());
        parts.push("");
    });

    parts.push('define("_ujgUserActivity", ["_ujgUA_main"], function(MyGadget) {');
    parts.push('    "use strict";');
    parts.push("    return MyGadget;");
    parts.push("});");
    parts.push("");

    var fullContent = parts.join("\n");

    fs.writeFileSync(TEMP_OUTPUT_FILE, fullContent, "utf8");
    fs.renameSync(TEMP_OUTPUT_FILE, OUTPUT_FILE);

    console.log("✓ Built successfully:", OUTPUT_FILE);
    console.log("  Total size:", (fullContent.length / 1024).toFixed(2), "KB");
    console.log("  Modules processed:", MODULE_ORDER.filter(function(f) {
        return fs.existsSync(path.join(MODULES_DIR, f));
    }).length);
}

if (require.main === module) {
    build();
}

module.exports = { build: build };
