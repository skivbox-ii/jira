#!/usr/bin/env node
/**
 * Скрипт сборки ujg-story-browser.js из модулей
 * Использование: node build-story-browser.js
 */

var fs = require("fs");
var path = require("path");

var MODULES_DIR = path.join(__dirname, "ujg-story-browser-modules");
var OUTPUT_FILE = path.join(__dirname, "ujg-story-browser.js");

var MODULE_ORDER = [
    "config.js",
    "utils.js",
    "storage.js",
    "api.js",
    "data.js",
    "rendering.js",
    "main.js"
];

function readModule(fileName) {
    var filePath = path.join(MODULES_DIR, fileName);
    if (!fs.existsSync(filePath)) {
        throw new Error("Module not found: " + fileName);
    }
    return fs.readFileSync(filePath, "utf8");
}

function build() {
    console.log("Building ujg-story-browser.js from modules...");

    var parts = [
        "// Auto-generated file - DO NOT EDIT MANUALLY",
        "// Generated by build-story-browser.js",
        "// To modify, edit files in ujg-story-browser-modules/ and rebuild",
        ""
    ];

    MODULE_ORDER.forEach(function(fileName) {
        var content = readModule(fileName);
        parts.push("/* === Module: " + fileName + " === */");
        parts.push(content.trim());
        parts.push("");
    });

    parts.push('define("_ujgStoryBrowser", ["_ujgSB_main"], function(G) {');
    parts.push('    "use strict";');
    parts.push("    return G;");
    parts.push("});");
    parts.push("");

    var fullContent = parts.join("\n");

    fs.writeFileSync(OUTPUT_FILE, fullContent, "utf8");

    console.log("✓ Built successfully:", OUTPUT_FILE);
    console.log("  Total size:", (fullContent.length / 1024).toFixed(2), "KB");
    console.log("  Modules processed:", MODULE_ORDER.length);
}

build.MODULE_ORDER = MODULE_ORDER.slice();

if (require.main === module) {
    build();
}

module.exports = { build: build };
