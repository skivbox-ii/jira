#!/usr/bin/env node
/**
 * Скрипт сборки ujg-daily-diligence.js из модулей
 * Использование: node build-daily-diligence.js
 */

var fs = require("fs");
var path = require("path");

var MODULES_DIR = path.join(__dirname, "ujg-daily-diligence-modules");
var OUTPUT_FILE = path.join(__dirname, "ujg-daily-diligence.js");

var MODULE_ORDER = [
    "config.js",
    "utils.js",
    "api-jira.js",
    "api-bitbucket.js",
    "api-confluence.js",
    "data-processor.js",
    "team-manager.js",
    "rendering.js",
    "main.js"
];

function readModule(fileName) {
    var filePath = path.join(MODULES_DIR, fileName);
    if (!fs.existsSync(filePath)) {
        throw new Error("Module not found: " + fileName);
    }
    return fs.readFileSync(filePath, "utf8");
}

function build() {
    console.log("Building ujg-daily-diligence.js from modules...");

    var parts = [
        "// Auto-generated file - DO NOT EDIT MANUALLY",
        "// Generated by build-daily-diligence.js",
        "// To modify, edit files in ujg-daily-diligence-modules/ and rebuild",
        ""
    ];

    MODULE_ORDER.forEach(function(fileName) {
        var content = readModule(fileName);
        if (!content) return;
        parts.push("/* === Module: " + fileName + " === */");
        parts.push(content.trim());
        parts.push("");
    });

    parts.push('define("_ujgDailyDiligence", ["_ujgDD_main"], function(MyGadget) {');
    parts.push('    "use strict";');
    parts.push("    return MyGadget;");
    parts.push("});");
    parts.push("");

    var fullContent = parts.join("\n");

    fs.writeFileSync(OUTPUT_FILE, fullContent, "utf8");

    console.log("✓ Built successfully:", OUTPUT_FILE);
    console.log("  Total size:", (fullContent.length / 1024).toFixed(2), "KB");
    console.log("  Modules processed:", MODULE_ORDER.filter(function(f) {
        return fs.existsSync(path.join(MODULES_DIR, f));
    }).length);
}

if (require.main === module) {
    build();
}

module.exports = { build: build };
